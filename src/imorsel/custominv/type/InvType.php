declare(strict_types=1);

namespace imorsel\custominv\type;

enum InvType : string{
    case CHEST = 'chest';
    case DOUBLE_CHEST = 'double_chest';
    case HOPPER = 'hopper';

    public function getSize() : int{
        return match($this){
            self::CHEST => 27,
            self::DOUBLE_CHEST => 54,
            self::HOPPER => 5,
        };
    }

    public function isDouble() : bool{
        return $this === self::DOUBLE_CHEST;
    }

    public function getTileId() : string{
        return match($this){
            self::CHEST, self::DOUBLE_CHEST => 'Chest',
            self::HOPPER => 'Hopper',
        };
    }
}
